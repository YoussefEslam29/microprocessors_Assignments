.MODEL SMALL 
.STACK 64 

.DATA
DATA_IN DB 10H, ,12H ,20H
SUM DB ? 
.CODE

MAIN PROC FAR 

MOV AX ,@DATA
MOV DS,AX 

MOV CX , 03
MOV AX , OFFSET DATA_IN
MOV AL , 0


AGAIN: ADD AL , [AX]
INC AX 
LOOP AGAIN

MOV SUM , AL


MOV AH,ACH
INT 21

MAIN ENDP
END MAIN

